# for loop - Good for looping a certain number of times

print('Hello')
for i in range(5):
    print('On this iteration, i is set to ' + str(i))
print('Goodbye')


# while loop - Good for looping whilst a certain condition is True

print('Hello')
i = 0
while i < 5:
    print('On this iteration, i is set to ' + str(i))
    i = i + 1
print('Goodbye')